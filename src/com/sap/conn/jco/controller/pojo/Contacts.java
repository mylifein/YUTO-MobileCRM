package com.sap.conn.jco.controller.pojo;

public class Contacts {
	private String surname ;    			//姓
	private String firstName ; 				//名
	private String bornDate ;				//出生日期
	private String duty ;					//职务
	private String department ;				//部门
	private String mobileNumber ;			//移动电话
	private String email ;					//email
	private String qqInfo ; 				//QQ
	private String weChat ;					//微信
	
	
	public String getSurname() {
		return surname;
	}
	public void setSurname(String surname) {
		this.surname = surname;
	}
	public String getFirstName() {
		return firstName;
	}
	public void setFirstName(String firstName) {
		this.firstName = firstName;
	}
	public String getBornDate() {
		return bornDate;
	}
	public void setBornDate(String bornDate) {
		this.bornDate = bornDate;
	}
	public String getDuty() {
		return duty;
	}
	public void setDuty(String duty) {
		this.duty = duty;
	}

	public String getMobileNumber() {
		return mobileNumber;
	}
	public void setMobileNumber(String mobileNumber) {
		this.mobileNumber = mobileNumber;
	}
	public String getEmail() {
		return email;
	}
	public void setEmail(String email) {
		this.email = email;
	}
	public String getQqInfo() {
		return qqInfo;
	}
	public void setQqInfo(String qqInfo) {
		this.qqInfo = qqInfo;
	}
	public String getWeChat() {
		return weChat;
	}
	public void setWeChat(String weChat) {
		this.weChat = weChat;
	}
	public String getDepartment() {
		return department;
	}
	public void setDepartment(String department) {
		this.department = department;
	}
	
	
	
	
	
	
	
	
	
}

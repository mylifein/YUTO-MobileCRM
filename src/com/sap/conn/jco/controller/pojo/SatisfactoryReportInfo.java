package com.sap.conn.jco.controller.pojo;

public class SatisfactoryReportInfo {
	private String cusMark;
	private String cusName;
	private String chargeClerk;
	private String belongDep;
	private String belongBU;
	private String totalScore;
	private String investigateDate;
	
	public String getCusMark() {
		return cusMark;
	}
	public void setCusMark(String cusMark) {
		this.cusMark = cusMark;
	}
	public String getCusName() {
		return cusName;
	}
	public void setCusName(String cusName) {
		this.cusName = cusName;
	}
	public String getChargeClerk() {
		return chargeClerk;
	}
	public void setChargeClerk(String chargeClerk) {
		this.chargeClerk = chargeClerk;
	}
	public String getBelongDep() {
		return belongDep;
	}
	public void setBelongDep(String belongDep) {
		this.belongDep = belongDep;
	}
	public String getBelongBU() {
		return belongBU;
	}
	public void setBelongBU(String belongBU) {
		this.belongBU = belongBU;
	}
	public String getTotalScore() {
		return totalScore;
	}
	public void setTotalScore(String totalScore) {
		this.totalScore = totalScore;
	}
	public String getInvestigateDate() {
		return investigateDate;
	}
	public void setInvestigateDate(String investigateDate) {
		this.investigateDate = investigateDate;
	}
	
	
}
